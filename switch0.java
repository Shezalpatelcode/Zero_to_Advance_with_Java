public class switch0 {
    public static void main(String[]args){
        int number=2;
        switch(number){
            case 1:System.out.println("one");
              break;
            case 2:System.out.println("Two");
              break;
            case 3:System.out.println("Three");
              break;
            default:
            System.out.println("Not even the number");
        }

    }
}
